void
Create()
{
  for (int i = 0; i < kMapHeight; ++i) {
    for (int j = 0; j < kMapWidth; ++j) {
      Tile* tile = &kTilemap.map[i][j];
      tile->type = (TileType)kDefaultMap[i][j];
      tile->x = j;
      tile->y = i;
    }
  }
}

